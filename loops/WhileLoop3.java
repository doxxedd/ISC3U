package loops;
public class WhileLoop3 {

	public static void main(String[] args) {
		int counter = -1;
		
		while (counter < 100) {
			counter ++;
			System.out.println(counter);
		}
		int counter2 = 100;
		while (counter2 > 0 ) {
			counter2 --;
			System.out.println(counter2);
		}
		
	}

}